# Operadores
print('Suma:', 3 + 4)
print('Resta:', 3 - 4)
print('Multiplicacion:', 3 * 4)
print('Division entera:', 4 // 3)
print('Modulo', 4 % 3)
print('Division decimal: ',4 / 3)
print('Potencia:' ,3 ** 4)

# Comparadores

print('Igualacion:', 3 == 4)
print('Diferente:', 3 != 4)
print('Mayor que:', 3 > 4)
print('Mayor o igual que:', 4 >= 3)
print('Menor que', 4 < 3)
print('Menor o igual que: ',4 <= 3)

# Operadores logicos
print('AND:', 1 == 3 and 4 != 1)
print('OR:', 3 != 2 or 4 == 4)

