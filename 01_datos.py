x = 'Hola'
print(x,type(x))

# Tipado dinamico
x = 34
print(x,type(x))

# Tipado estatico (OTROS LENGUAJES), no existe en Python

# Tipado float
x = 34.78
print(x,type(x))

# Tipado palabras reservadas
x = True
print(x,type(x))

x = False
print(x,type(x))

x = None
print(x,type(x))

# Tipos complejos
# listas, arrays, arreglos
c = ['Pepe', 23, True] 
print(c,type(c))

# tupla, array inmutable
c = ('Pepe', 23, True) 
print(c,type(c))

# set, array de elementos Ãºnicos
c = {'Pepe', 23, True} 
print(c,type(c))

# diccionarios, array asociativos, hash, objetos, literales
c = {'nombre':'Pepe','edad':'23','isAlumno':True} 
print(c,type(c))